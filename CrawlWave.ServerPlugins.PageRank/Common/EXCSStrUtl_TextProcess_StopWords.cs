		static public bool ExistsInEnglishStopWords(string strWord)
		{
			return (Instance.slStopWordsEnglish.IndexOfKey(strWord)>-1);
		}

		static public bool ExistsInGreekStopWords(string strWord)
		{
			return (Instance.slStopWordsGreek.IndexOfKey(strWord)>-1);
		}

		static public bool ExistsInAllStopWords(string strWord)
		{
			return ( ExistsInGreekStopWords(strWord) || ExistsInEnglishStopWords(strWord) );
